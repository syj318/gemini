def search_bexco_info(query):
    """벡스코 정보에서 검색"""
    query = query.lower()
    results = []
    
    for category, data in BEXCO_DATA.items():
        if isinstance(data, dict):
            for subcategory, subdata in data.items():
                if isinstance(subdata, dict):
                    for key, value in subdata.items():
                        if query in str(key).lower() or query in str(value).lower():
                            results.append(f"{category} > {subcategory} > {key}: {value}")
                else:
                    if query in str(subcategory).lower() or query in str(subdata).lower():
                        results.append(f"{category} > {subcategory}: {subdata}")
        else:
            if query in str(category).lower() or query in str(data).lower():
                results.append(f"{category}: {data}")
    
    return results
